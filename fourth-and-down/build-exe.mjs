lines.push(`      total += runPts; details.push("Runs: " + mult + " run(s) of " + bestLen + " = " + runPts + " pts");`);
lines.push(`    }`);
lines.push(`    // Flush`);
lines.push(`    const handSuit = hand[0].suit;`);
lines.push(`    if (hand.every(c => c.suit === handSuit)) {`);
