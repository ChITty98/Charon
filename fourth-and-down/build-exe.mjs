lines.push(`app.post("/api/cribbage/calculate", (req, res) => {`);
lines.push(`  try {`);
lines.push(`    const { hand, cut } = req.body;`);
lines.push(`    // hand = [{rank, suit}, ...], cut = {rank, suit}`);
lines.push(`    if (!hand || hand.length !== 4 || !cut) return res.status(400).json({ error: "Need 4 hand cards and 1 cut" });`);
lines.push(`    const all = [...hand, cut];`);
lines.push(`    const cv = (r) => r >= 10 ? 10 : r;`);
lines.push(`    let total = 0; const details = [];`);
lines.push(`    // Fifteens`);
lines.push(`    let fifteenCount = 0;`);
lines.push(`    for (let mask = 1; mask < 32; mask++) {`);
lines.push(`      let sum = 0;`);
lines.push(`      for (let i = 0; i < 5; i++) { if (mask & (1 << i)) sum += cv(all[i].rank); }`);
lines.push(`      if (sum === 15) fifteenCount++;`);
lines.push(`    }`);
lines.push(`    if (fifteenCount > 0) { total += fifteenCount * 2; details.push("Fifteens: " + fifteenCount + " = " + (fifteenCount * 2) + " pts"); }`);
lines.push(`    // Pairs`);
lines.push(`    let pairCount = 0;`);
lines.push(`    for (let i = 0; i < 5; i++) for (let j = i+1; j < 5; j++) if (all[i].rank === all[j].rank) pairCount++;`);
lines.push(`    if (pairCount > 0) { total += pairCount * 2; details.push("Pairs: " + pairCount + " = " + (pairCount * 2) + " pts"); }`);
lines.push(`    // Runs - use unique ranks, find longest consecutive, multiply by counts`);
lines.push(`    const uniqueRanks = [...new Set(all.map(c => c.rank))].sort((a,b) => a-b);`);
lines.push(`    let bestStart = 0, bestLen = 1, curStart = 0, curLen = 1;`);
lines.push(`    for (let i = 1; i < uniqueRanks.length; i++) {`);
lines.push(`      if (uniqueRanks[i] === uniqueRanks[i-1]+1) { curLen++; } else {`);
